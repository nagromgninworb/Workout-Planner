def greaterThan(x, y):  # Defining the function greaterThan with two parameters x and y
    return x > y        # Returns True if x is greater than y, otherwise False

a = 2  # Assigning 2 to variable a
b = 3  # Assigning 3 to variable b

result = greaterThan(a, b)  # Calling the function and storing the result in the variable result

# Printing the formatted output
print("The statement " + str(a) + " is greater than " + str(b) + " is " + str(result))
